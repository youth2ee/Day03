package com.naver.control3;

public class Fortest8 {

	public static void main(String[] args) {
		
		//for(초기식;조건식;증감식){}
		
		for(int i=0;i<5;i++) {
			//for문에 대해서 고정관념을 가지면 안된다. 기본원리에 집중.
			//조건식 : 꼭 상수아니어도 된다. 연산자도 가능. 변수도 가능. true,false만 나오면 된다.
			//for(;;){} : 무한반복문
			//for(;true;){} : 무한반복문
			
			System.out.println(i);
		} 
		
		
		
		
	}
	
}
